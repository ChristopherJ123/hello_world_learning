package com.guramee;

import java.util.Arrays;

public class ForEach {
    public static void main (String[] args) {
        String fruits[] = { "Apple", "Orange", "Grapes", "Banana", "Mango"};

        for (int i = 0; i < fruits.length ; i++) {
            System.out.println(fruits[i]);
        }
    }
}
